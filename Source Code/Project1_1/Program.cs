        static int DijkstraForIzmir(int start, int target, int[,] izmirDistances, Dictionary<int, List<int>> izmirNeighbors) // Dijkstra's algorithm for the districts of Izmir

        {
            int n = izmirDistances.GetLength(0);
            int[] distances = new int[n];
            bool[] visited = new bool[n];
            for (int i = 0; i < distances.Length; i++)
            {
                distances[i] = int.MaxValue;
            }
            distances[start] = 0;

            for (int i = 0; i < n; i++)
            {
                int u = -1;
                for (int j = 0; j < n; j++)
                {
                    if (!visited[j] && (u == -1 || distances[j] < distances[u]))
                    {
                        u = j;
                    }
                }

                if (u == -1 || distances[u] == int.MaxValue)
                    break;

                visited[u] = true;

                foreach (var v in izmirNeighbors[u])
                {
                    int weight = izmirDistances[u, v];
                    if (weight != int.MaxValue && distances[u] + weight < distances[v])
                    {
                        distances[v] = distances[u] + weight;
                    }
                }
            }
            return distances[target];
        }

        static void FillDistanceMatrix(int[,] matrix, int[][] distances, Dictionary<int, List<int>> neighbors) // Adjusts the distance matrix for use with Dijkstra's algorithm


        {
            for (int i = 0; i < 81; i++)
            {
                for (int j = 0; j < 81; j++)
                {
                    if (i == j)
                    {
                        matrix[i, j] = 0;
                    }
                    else if (neighbors.ContainsKey(i + 1) && neighbors[i + 1].Contains(j + 1))
                    {
                        matrix[i, j] = GetDistance(distances, i, j);
                    }
                    else
                    {
                        matrix[i, j] = int.MaxValue;
                    }
                }
            }
        }

        static int GetDistance(int[][] distances, int city1, int city2)  // Retrieves the distance between two cities from the jagged array

        {
            return city1 < city2 ? distances[city2][city1] : distances[city1][city2];
        }

        static int Dijkstra(int start, int target, Dictionary<int, List<int>> neighbors, int[,] distanceMatrix)  // Dijkstra's algorithm implementation

        {
            int n = distanceMatrix.GetLength(0);
            int[] distances = new int[n];
            bool[] visited = new bool[n];
            for (int i = 0; i < distances.Length; i++)
            {
                distances[i] = int.MaxValue;
            }
            distances[start] = 0;

            for (int i = 0; i < n; i++)
            {
                int u = -1;
                for (int j = 0; j < n; j++)
                {
                    if (!visited[j] && (u == -1 || distances[j] < distances[u]))
                    {
                        u = j;
                    }
                }

                if (distances[u] == int.MaxValue)
                    break;

                visited[u] = true;

                foreach (var v in neighbors[u + 1])
                {
                    int weight = distanceMatrix[u, v - 1];
                    if (weight != int.MaxValue && distances[u] + weight < distances[v - 1])
                    {
                        distances[v - 1] = distances[u] + weight;
                    }
                }
            }
            return distances[target];
        }
